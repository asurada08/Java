package day04;

public class Ch04_04 {

	public static void main(String[] args) {
		int a, b;
		a = 100;
		b = 200;
		
		System.out.printf("%d == %d = %s이다\n", a, b, a==b);
		System.out.printf("%d != %d = %s이다\n", a, b, a!=b);
		System.out.printf("%d > %d = %s이다\n", a, b, a>b);
		System.out.printf("%d < %d = %s이다\n", a, b, a<b);
		System.out.printf("%d >= %d = %s이다\n", a, b, a>=b);
		System.out.printf("%d <= %d = %s이다\n", a, b, a<=b);
		System.out.printf("%d = %d = %s이다\n", a, b, a=b);

	}

}
