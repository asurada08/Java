package day04;

public class Ch04_02 {

	public static void main(String[] args) {
		int a=10;
		
		a++;
		System.out.println("a++ = " + a);
		
		a--;
		System.out.println("a-- = " + a);
		
		a += 5;
		System.out.println("a += 5 = "+ a);
		
		a -= 5;
		System.out.println("a -= 5 = "+ a);
		
		a *= 5;
		System.out.println("a *= 5 = "+ a);
		
		a /= 5;
		System.out.println("a /= 5 = "+ a);
		
		a %= 5;
		System.out.println("a %= 5 = "+ a);
	}

}
